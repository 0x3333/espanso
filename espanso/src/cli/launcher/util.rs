use std::path::Path;

use crate::lock::acquire_legacy_lock;

pub fn is_legacy_version_running(runtime_path: &Path) -> bool {
  let legacy_lock_file = acquire_legacy_lock(runtime_path);
  if legacy_lock_file.is_none() {
    true 
  } else {
    false 
  }
}